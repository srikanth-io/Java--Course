public class Literals {
    public static void main(String[] args) {
    
        int x = 100;

        // Decimal Literals - 0 to 9
        int x1 = 146;

        // Octal Literals - begins with 0 and allows only 0 to 7
        int x2 = 0124;

        // Hexa decimal Literals - 0 to 9 and characters a to f, here only java is Non-case sensitive
        int x3 = 0x123facE;

        // Binary Literals - 0 and 1 literals should be perfixed 0b or 0B
        int x4 = 0b1111;
    }
}
